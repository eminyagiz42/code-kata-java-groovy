package com.codekata.hackerrank.problemsolving.magic;

import java.util.List;

public class SumAnalysisModel {

    private MatrixSection matrixSection;
    private int order;
    private int sum;
    private List<String> coordinateList;

    public MatrixSection getMatrixSection() {
        return matrixSection;
    }

    public void setMatrixSection(MatrixSection matrixSection) {
        this.matrixSection = matrixSection;
    }

    public int getOrder() {
        return order;
    }

    public void setOrder(int order) {
        this.order = order;
    }

    public int getSum() {
        return sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }

    public List<String> getCoordinateList() {
        return coordinateList;
    }

    public void setCoordinateList(List<String> coordinateList) {
        this.coordinateList = coordinateList;
    }


}
